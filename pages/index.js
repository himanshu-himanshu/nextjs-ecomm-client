import Head from "next/head";
import { BsArrowUpCircleFill } from "react-icons/bs";
import {
  Navbar,
  Banner,
  Quote,
  Trending,
  GenderSection,
  Footer,
  PopularCategories,
  Features,
} from "../components";
import { useEffect, useState } from "react";

export default function Home() {
  const [showArrow, setShowArrow] = useState(false);

  useEffect(() => {
    const handleScroll = () => {
      if (window.scrollY > 200) {
        setShowArrow(true);
      } else {
        setShowArrow(false);
      }
    };

    window.addEventListener("scroll", handleScroll);

    return () => {
      window.removeEventListener("scroll", handleScroll);
    };
  }, []);

  const scrollToTop = () => {
    window.scrollTo({
      top: 0,
      behavior: "smooth",
    });
  };
  return (
    <>
      <Head>
        <title>GRAB.IT</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Navbar />
      <Banner />
      <Trending showTrending={false} />
      <GenderSection />
      <Quote quote="Fashions fade. Style is eternal." />
      <PopularCategories />
      <Features />
      <Footer />
      {showArrow && (
        <div
          className="fixed bottom-0 right-0 z-40 m-4 cursor-pointer border-0"
          onClick={scrollToTop}
        >
          <BsArrowUpCircleFill className="text-5xl text-yellow-700 rounded-full" />
        </div>
      )}
    </>
  );
}
